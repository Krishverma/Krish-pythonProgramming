# Implicit an explicit type conversion
a=int(7)
b=int(3)
print("a is assigned int 7")
print("b is assigned int 3")
print("b/a : ",b/a)
print("Here both variables assigned int but still answer comes in float datatype.")

print("This type of conversion is called Implicit Conversion")

print("int b/a :",int(b/a))
print("Here programmer specifed the answer to be in integer")
print("this type of conversion is colled explixit conversion")
