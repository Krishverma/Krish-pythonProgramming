#color name from first letter
a=str(input("Enter I letter: "))
if(a=='v' or a=='V'):
    print("Violet")
elif(a=='i' or a=='I'):
    print("Indigo")
elif(a=='b' or a=='B'):
    print("Blue")
elif(a=='g' or a=='G'):
    print("Green")
elif(a=='y' or a=='Y'):
    print("Yellow")
elif(a=='o' or a=='O'):
    print("Orange")
elif(a=='r' or a=='R'):
    print("Red")
else:
    print("Color Not Found")
